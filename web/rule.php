<div class="textbox-title">竞赛规则</div>
<div class="textbox-content">
<p class="title">(一)网络预选：</p>
<p class="content">网络预选采取接受任意3人组合报名方式，经验证后统一发放参赛ID和密码。网络预赛将最后参赛队伍压缩至最多80支队伍，校内、校外各40支。</p>
<p class="content">1.武汉大学校内队伍以网络赛排序为序选取不超过40支队伍参加决赛。</p>
<p class="content">2.对成绩最好以及参加正式ACM/ICPC竞赛的学校选取不超过40支队伍进行邀请。</p>
<p class="title">(二)队长会议：</p>
<p class="content">宣布规则，抽签选机位，公布题目数目及最终题目与气球的对应关系，提供参赛指南，回答任意与比赛有关的提问。</p>
<p class="title">(三)练习赛：</p>
<p class="content">练习赛为选手以及裁判熟悉竞赛系统及熟悉赛场准备，开赛1小时内规则同正式比赛，1小时后可交流。</p>
<p class="title">(四)正式赛：</p>
<p class="content">正式赛比赛过程中一队三人使用一台计算机，允许携带一切纸质参考资料，禁止一切电子参考资料及产品入内（包括计算器、电子词典、PDA等）。</p>
<p class="content">正式赛最终排名以队伍通过题目数目排序，通过题目多者排前面，通过题目数目相同队伍的总时间少者排前面。每队使用时间为所有题通过总时间加上总共罚时，通过时间指通过该题距比赛开始的时间，罚时指任意一个题的未通过提交均需罚时20分钟，该题在比赛结束时仍未通过不算罚时。通过一题指该队提交的代码正确编译运行后能完全正确处理并输出黑箱测试的输入数据。</p>
<p class="content">正式赛提供实时排名给参赛队。每队通过一题时，由工作人员将一个该题对应颜色的气球插至该队的花盆中。竞赛中所有队伍可通过观看其他队的气球情况判断通过情况。竞赛中所有队伍均可请求打印代码，网络打印机打印后由工作人员送至请求队伍处。</p>
<p class="title">(五)获奖评比：</p>
<p class="content">1．比赛排名：</p>
<p class="content">分为校内队伍和校外队伍，分别排名并评出一等奖三队、二等奖六队、三等奖九队，颁发证书、奖金及奖品。</p>
<p class="content">2．官方排名：</p>
<p class="content">按照比赛总排名给出官方排名并打印、颁发官方授权证书（由于此工作相对比较延迟，采用邮寄方式颁发证书给获奖队伍）。</p>
<p class="title">(六)   竞赛地点、编程语言、环境：</p>
<p class="content">1、预赛：</p>
<p class="content">武汉大学oak在线评测系统，支持C/C++/Java/Pascal</p>
<p class="content">2、练习赛、正式赛：</p>
<p class="content">武汉大学信息学部计算机学院实验室，Windows2000，支持C / C++ / Java / Pascal。可使用开发环境为gVim、Eclipse、Devcpp4.9.9.2、free pascal。</p>
